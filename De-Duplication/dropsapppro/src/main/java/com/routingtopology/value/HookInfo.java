package com.routingtopology.value;

public class HookInfo {

	private int hookId;

	private double hookValue;

	/**
	 * @param hookId
	 */
	public void setHookId(int hookId) {
		this.hookId = hookId;
	}

	/**
	 * @return hook id
	 */
	public int getHookId() {
		return hookId;
	}

	/**
	 * @param hookValue
	 */
	public void setHookValue(double hookValue) {
		this.hookValue = hookValue;
	}

	/**
	 * @return Value of the Hook
	 */
	public double getHookValue() {
		return hookValue;
	}

}
