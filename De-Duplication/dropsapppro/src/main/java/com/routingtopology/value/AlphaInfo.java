package com.routingtopology.value;

/**
 * This is the Information
 * of the Alpha Constant
 *
 */
public class AlphaInfo {
	
	private int nodeId;
	
	/**
	 * @param nodeId
	 */
	public void setNodeId(int nodeId) {
		this.nodeId = nodeId;
	}

	/**
	 * @return Integer  i.e ID of the Node
	 */
	public int getNodeId() {
		return nodeId;
	}
	
	private double alpha;
	/**
	 * @param alpha
	 */
	public void setAlpha(double alpha) {
		this.alpha = alpha;
	}

	/**
	 * @return Double an alpha value
	 */
	public double getAlpha() {
		return alpha;
	}

	

	

}
