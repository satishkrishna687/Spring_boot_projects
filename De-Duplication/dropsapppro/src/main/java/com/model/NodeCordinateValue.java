package com.model;



/**
 * 
 * position of the Node
 * In a given Network
 *  *
 */
public class NodeCordinateValue {
	
	
	private int x_cor;
	
	/**
	 * @return The X-cordinates of the node
	 */
	public int getX_cor() {
		return x_cor;
	}

	/**
	 * @param xCor -set the x-cordinates of the node
	 */
	public void setX_cor(int xCor) {
		x_cor = xCor;
	}

	/**
	 * @return Y-cordinate of the node
	 */
	public int getY_cor() {
		return y_cor;
	}

	/**
	 * @param yCor
	 */
	public void setY_cor(int yCor) {
		y_cor = yCor;
	}

	private int y_cor;

}
